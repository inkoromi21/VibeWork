def normalize_role(role_or_sphere: str) -> str:
    """Принимает ключ сценария или id сферы из анкеты; возвращает ключ из _STORIES."""
    key = (role_or_sphere or "").strip()
    if not key:
        return "analyst"
    if key in SPHERE_TO_SIM:
        key = SPHERE_TO_SIM[key]
    if key in _STORIES:
        return key
    return "analyst"


def _choice_label(step: Dict[str, Any], choice_id: str) -> str:
    for c in step.get("choices") or []:
        if c.get("id") == choice_id:
            return str(c.get("label") or choice_id)
    return choice_id


def _choice_insight(step: Dict[str, Any], choice_id: str) -> str:
    raw = (step.get("insights") or {}).get(choice_id)
    return str(raw or "").strip()


def _scenario_max_points(steps: List[Dict[str, Any]]) -> int:
    total = 0
    for s in steps:
        pts = s.get("points") or {}
        if not pts:
            continue
        total += max(int(v) for v in pts.values())
    return total


def _format_day_recap(
    path: List[Dict[str, Any]],
    total_points: int,
    closing: str,
    max_points: int,
) -> str:
    lines: List[str] = ["Как вы прошли день:", ""]
    for i, item in enumerate(path, 1):
        scene = str(item.get("scene") or "").strip()
        choice = str(item.get("choice") or "").strip()
        pts = int(item.get("points") or 0)
        insight = str(item.get("insight") or "").strip()
        lines.append(f"{i}. {scene}")
        lines.append(f"   → Ваш выбор: «{choice}» (+{pts} оч.).")
        if insight:
            lines.append(f"   Отклик: {insight}")
        lines.append("")
    lines.append(f"Итого за день: {total_points} карьерных очков.")
    lines.append(
        f"В этом сценарии можно набрать до {max_points} оч. "
        "(сумма лучших вариантов на каждом шаге; «лучший» здесь — про сочетание риска, скорости и пользы для людей и продукта, а не единственно верный ответ)."
    )
    if max_points > 0 and total_points >= max_points:
        lines.append("У вас максимум по этому проходу — сильный набор решений.")
    elif max_points > 0 and total_points == max_points - 1:
        lines.append("Почти максимум: перечитайте отклики на шаге с меньшим баллом — там обычно виден компромисс.")
    lines.append(closing)
    return "\n".join(lines)


def list_simulator_options() -> List[Dict[str, str]]:
    """Одна строка на каждую рекомендуемую сферу — как в главной сфере анкеты."""
    out: List[Dict[str, str]] = []
    for s in INTEREST_SPHERES:
        sid = s["id"]
        label = s["label"]
        role = normalize_role(sid)
        out.append({"sphere_id": sid, "label": label, "role": role})
    return out


def start(role: str) -> Dict[str, Any]:
    r = normalize_role(role)
    steps = _STORIES.get(r) or _STORIES["analyst"]
    first = steps[0]
    return {
        "role": r,
        "step_index": 0,
        "career_points": 0,
        "day_path": [],
        "node": {
            "text": first["text"],
            "choices": first["choices"],
        },
        "done": False,
    }


def step(
    role: str,
    step_index: int,
    career_points: int,
    choice_id: str,
    day_path: List[Dict[str, Any]] | None = None,
) -> Dict[str, Any]:
    r = normalize_role(role)
    steps = _STORIES.get(r) or _STORIES["analyst"]
    idx = max(0, min(len(steps) - 1, int(step_index)))
    cur = steps[idx]
    add = int(cur.get("points", {}).get(choice_id, 1))
    new_points = career_points + add
    path = list(day_path) if day_path else []
    insight = _choice_insight(cur, choice_id)
    path.append(
        {
            "scene": cur.get("text") or "",
            "choice": _choice_label(cur, choice_id),
            "points": add,
            "insight": insight,
        }
    )
    next_idx = idx + 1
    closing = "День закончен. Сохраняйте баланс глубины, коммуникации и скорости — так растёт и реальная карьера."
    max_pts = _scenario_max_points(steps)
    if next_idx >= len(steps):
        return {
            "role": r,
            "step_index": next_idx,
            "career_points": new_points,
            "day_path": path,
            "node": None,
            "done": True,
            "summary": closing,
            "day_recap": _format_day_recap(path, new_points, closing, max_pts),
            "scenario_max_points": max_pts,
        }
    nxt = steps[next_idx]
    return {
        "role": r,
        "step_index": next_idx,
        "career_points": new_points,
        "day_path": path,
        "node": {"text": nxt["text"], "choices": nxt["choices"]},
        "done": False,
        "last_choice_insight": insight,
    }
