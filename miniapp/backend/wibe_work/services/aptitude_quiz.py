def _interest_key(interest: str) -> str:
    from wibe_work.services.aptitude_quiz_content_bridge import normalize_sphere_id

    k = normalize_sphere_id(interest)
    return k if k in _PROFILE_BY_INTEREST else "default"


def _expert_block_for_grade(grade: str) -> List[Dict[str, Any]]:
    g = (grade or "university").strip().lower()
    if g == "school":
        return _EXPERT_SCHOOL
    if g == "vocational":
        return _EXPERT_VOCATIONAL
    return _EXPERT_JOB_SEARCH


def _technical_questions(interest: str) -> List[Dict[str, Any]]:
    from wibe_work.services.aptitude_quiz_content_bridge import fetch_technical_from_website

    web = fetch_technical_from_website(interest)
    if web and len(web) >= TECHNICAL_COUNT:
        return web[:TECHNICAL_COUNT]

    prof = copy.deepcopy(_PROFILE_BY_INTEREST[_interest_key(interest)])
    for q in prof:
        q["block"] = "technical"
    return prof[:TECHNICAL_COUNT]


def _personality_questions(interest: str, grade: str) -> List[Dict[str, Any]]:
    from wibe_work.services.aptitude_quiz_content_bridge import personality_track_for_interest

    expert = copy.deepcopy(_expert_block_for_grade(grade))
    g = (grade or "university").strip().lower()
    if g != "university":
        track = personality_track_for_interest(interest)
        q12 = _SPHERE_EXPERT_Q12.get(track) or _SPHERE_EXPERT_Q12["general"]
        for q in expert:
            if q.get("id") == 12 and q12:
                q["text"] = q12
    for q in expert:
        q["block"] = "career" if g == "university" else "personality"
    return expert


def get_quiz_bundle(
    interest: str,
    grade: str = "university",
    profile: Dict[str, Any] | None = None,
) -> Dict[str, Any]:
    """Модули профориентации + 10 по сфере + 5 карьерных (по профилю или grade)."""
    if profile:
        from wibe_work.services.assessment_bundle import get_assessment_bundle

        return get_assessment_bundle(profile, interest)
    technical = _technical_questions(interest)
    personality = _personality_questions(interest, grade)
    combined = technical + personality
    return {
        "interest": (interest or "").strip() or "other",
        "test_grade": (grade or "university").strip().lower(),
        "technical_count": len(technical),
        "personality_count": len(personality),
        "technical": technical,
        "personality": personality,
        "questions": combined,
        "orientation_count": 0,
        "total_count": len(combined),
        "core_offset": 0,
    }


def get_questions_for_interest(
    interest: str,
    grade: str = "university",
    profile: Dict[str, Any] | None = None,
) -> List[Dict[str, Any]]:
    """Совместимость: плоский список вопросов опроса."""
    return get_quiz_bundle(interest, grade, profile=profile)["questions"]


def get_pro_weights_matrix_for_interest(
    interest: str,
    profile: Dict[str, Any] | None = None,
) -> WRow:
    """Строки весов в порядке question_id (с учётом orientation-блока в профиле)."""
    if profile:
        from wibe_work.services.assessment_bundle import get_assessment_bundle

        bundle = get_assessment_bundle(profile, interest)
        return list(bundle.get("weights_matrix") or [])
    k = _interest_key(interest)
    tech_w = _WEIGHT_PROFILE.get(k, _W_DEFAULT)
    return list(tech_w) + list(_EXPERT_WEIGHTS)


def letter_to_index(letter: str) -> int:
    m = {"A": 0, "B": 1, "C": 2, "D": 3}
    return m.get((letter or "A").upper(), 0)
