def build_analysis_user_prompt(
    *,
    profile_summary: str,
    readiness_percent: int,
    axes_line: str,
    plan_line: str,
    answers_count: int,
    short_quiz_caveat: str,
) -> str:
    """Собирает развёрнутый пользовательский промпт для нарратива разбора."""
    lines = [
        "Ниже — данные расчёта приложения VibeWork. Напиши финальный текст для экрана «ИИ-разбор» по правилам из system.",
        "",
        "=== ПРОФИЛЬ (КРАТКО) ===",
        profile_summary.strip(),
        "",
        "=== МЕТРИКИ ПРИЛОЖЕНИЯ ===",
        f"Индекс готовности (уже посчитан приложением): {readiness_percent}%",
        f"Оси методики / стиля с процентами: {axes_line}",
        f"Три направления развития A/B/C (названия и проценты из модели): {plan_line}",
        f"Число ответов в опросе: {answers_count}",
    ]
    if short_quiz_caveat.strip():
        lines.extend(["", "=== ОГРАНИЧЕНИЕ ПО ОПРОСУ ===", short_quiz_caveat.strip()])
    lines.extend(
        [
            "",
            "=== ЗАДАЧА ===",
            "Сгенерируй один связный текст по системным правилам: квартальный фокус, риск перегруза/выгорания, практичность. Не перечисляй все числа подряд — используй их осмысленно.",
        ]
    )
    return "\n".join(lines)


def build_chat_user_instruction_suffix() -> str:
    """Фрагмент, добавляемый к пользовательскому промпту чата (опционально)."""
