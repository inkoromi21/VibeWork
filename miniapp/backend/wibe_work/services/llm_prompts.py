def _last_user_text(messages: list[dict]) -> str:
    for m in reversed(messages):
        if (m.get("role") or "") == "user":
            return str(m.get("content", "")).strip()
    if messages:
        return str(messages[-1].get("content", "")).strip()
    return ""


def _grade_layer_addendum(education_grade: str) -> str:
    g = (education_grade or GRADE_UNIVERSITY).strip().lower()
    if g == GRADE_SCHOOL:
        return CHAT_ADDENDUM_GRADE_SCHOOL
