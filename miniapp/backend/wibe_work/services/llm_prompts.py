def polish_analysis_narrative(text: str, *, max_chars: int = 480) -> str:
    """Обрезка и лёгкая чистка ИИ-нарратива разбора перед показом в UI."""
    import re

    t = (text or "").strip()
    if not t:
        return t
    t = re.sub(r"^#+\s*", "", t, flags=re.MULTILINE)
    t = re.sub(r"^\s*[-•*]\s+", "", t, flags=re.MULTILINE)
    t = re.sub(r"\s+", " ", t).strip()
    if len(t) > max_chars:
        cut = t[: max_chars + 1]
        last = max(cut.rfind(". "), cut.rfind("! "), cut.rfind("? "))
        t = (cut[: last + 1] if last > max_chars // 2 else cut[:max_chars]).strip()
    return t


