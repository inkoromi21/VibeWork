def infer_education_grade_from_text(blob: str) -> str:
    """Эвристика по тексту summary/контекста (веб-чат без объекта profile)."""
    low = (blob or "").lower()
    if (
        "школьный уровень" in low
        or "режим: школьник" in low
        or "8–11 класс" in low
        or "школьник (8" in low
    ):
        return GRADE_SCHOOL
    if ("спо" in low or "колледж" in low) and "школьник" not in low:
        return GRADE_VOCATIONAL
    return GRADE_UNIVERSITY


