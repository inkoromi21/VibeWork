def _last_user_text(messages: list[dict]) -> str:
    for m in reversed(messages):
        if (m.get("role") or "") == "user":
            return str(m.get("content", "")).strip()
    if messages:
        return str(messages[-1].get("content", "")).strip()
    return ""


def _grade_layer_addendum(education_grade: str) -> str:
    g = (education_grade or GRADE_UNIVERSITY).strip().lower()
    if g == GRADE_SCHOOL:
        return CHAT_ADDENDUM_GRADE_SCHOOL
    if g == GRADE_VOCATIONAL:
        return CHAT_ADDENDUM_GRADE_VOCATIONAL
    return CHAT_ADDENDUM_GRADE_UNIVERSITY


def select_chat_addenda(
    last_user: str,
    profile: dict | None,
    analysis_snap: dict | None,
    *,
    education_grade: str = GRADE_UNIVERSITY,
) -> list[str]:
    """Какие situational-блоки добавить к system."""
    grade = (education_grade or GRADE_UNIVERSITY).strip().lower()
    out: list[str] = [_grade_layer_addendum(grade)]
    if not analysis_snap:
        out.append(
            CHAT_ADDENDUM_NO_ANALYSIS_SCHOOL
            if grade == GRADE_SCHOOL
            else CHAT_ADDENDUM_NO_ANALYSIS
        )
    profile = profile or {}
    pain = (profile.get("primary_pain") or "").strip()
    if pain == "pain_no_exp" and grade == GRADE_SCHOOL:
        out.append(CHAT_ADDENDUM_PAIN_NO_EXP_SCHOOL)
    elif pain in _PAIN_ADDENDA:
        out.append(_PAIN_ADDENDA[pain])
    low = last_user.lower()
    seen_topics: set[str] = set()
    for keys, topic in _TOPIC_KEYWORDS:
        if topic in seen_topics:
            continue
        if any(k in low for k in keys):
            seen_topics.add(topic)
            if topic == "jobs":
                if grade == GRADE_SCHOOL:
                    out.append(CHAT_ADDENDUM_JOBS_SCHOOL)
                else:
                    out.append(CHAT_ADDENDUM_JOBS)
            elif topic == "learning":
                out.append(CHAT_ADDENDUM_LEARNING)
    if len(last_user) < 40 and any(
        w in low for w in ("привет", "здравств", "добрый", "hi", "hello", "хай")
    ):
        out.append(CHAT_ADDENDUM_GREETING)
    return out


def build_chat_system_prompt(
    addenda: list[str] | None = None,
    *,
    context_pack: str = "",
    education_grade: str = GRADE_UNIVERSITY,
) -> str:
    """Базовый system по уровню образования + контекст + ситуационные дополнения."""
    parts = [coach_chat_system_for_grade(education_grade)]
    ctx = (context_pack or "").strip()
    if ctx:
        parts.append(
            "=== ДАННЫЕ ПОЛЬЗОВАТЕЛЯ (анкета, разбор, материалы — используй при каждом ответе) ===\n"
            + ctx
        )
    if addenda:
        unique = []
        seen: set[str] = set()
        for a in addenda:
            if a not in seen:
                seen.add(a)
                unique.append(a)
        parts.append("\n\n".join(unique))
    return "\n\n".join(parts)


