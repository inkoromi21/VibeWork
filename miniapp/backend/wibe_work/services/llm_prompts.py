def _append_sphere_layer(
    base: str,
    profile: dict | None,
    *,
    interest: str = "",
    education_grade: str = GRADE_UNIVERSITY,
    analysis_mode: str = "",
) -> str:
    from wibe_work.services.sphere_prompts import build_sphere_prompt_layer

    layer = build_sphere_prompt_layer(
        profile,
        interest=interest,
        education_grade=education_grade,
        analysis_mode=analysis_mode,
    )
    return f"{base}\n\n{layer}"


def narrative_system_for_grade(
    analysis_mode: str,
    education_grade: str,
    profile: dict | None = None,
    *,
    interest: str = "",
) -> str:
    if (analysis_mode or "").strip().lower() == "school" or education_grade == GRADE_SCHOOL:
        base = ANALYSIS_NARRATIVE_SCHOOL_SYSTEM
        mode = "school"
    elif education_grade == GRADE_VOCATIONAL:
        base = ANALYSIS_NARRATIVE_VOCATIONAL_SYSTEM
        mode = "career"
    else:
        base = ANALYSIS_NARRATIVE_SYSTEM
        mode = "career"
    if profile is not None:
        from wibe_work.services.sphere_prompts import school_subject_layer

        extra = school_subject_layer(profile) if mode == "school" else ""
        base = _append_sphere_layer(
            base, profile, interest=interest, education_grade=education_grade, analysis_mode=mode
        )
        if extra:
            base = f"{base}\n\n{extra}"
    return base


