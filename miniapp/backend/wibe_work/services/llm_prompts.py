    user_tail = (
        "\n\nПоследнее сообщение пользователя (ответь только на него):\n"
        f"«{last_user[:2000]}»"
    )
    prompt = "\n\n".join(parts + [f"История диалога:\n{transcript}", user_tail])
    return prompt + build_chat_user_instruction_suffix()


