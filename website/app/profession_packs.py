@dataclass(frozen=True)
class GradeRowTpl:
    code: str
    stage_name: str
    typical_roles: str
    level_up_criteria: str


@dataclass(frozen=True)
class ProfessionPack:
    """Один «пакет» под семейство профессий."""

    key: str
    grade_rows: tuple[GradeRowTpl, GradeRowTpl, GradeRowTpl, GradeRowTpl]
    insight_axis_titles: tuple[str, str, str]
    style_fit: tuple[tuple[str, str], tuple[str, str], tuple[str, str]]
    gap_headline: str
    gap_bar_labels: dict[str, str]
    first_weak: tuple[str, str, str]
    first_med: tuple[str, str, str]
    first_strong: tuple[str, str, str]
    salary_a: str
    salary_b: str
    salary_c: str


def _track(t: str) -> str:
    return (t or "направлении").strip()


def _g(
    code: str,
    stage: str,
    roles: str,
    crit: str,
) -> GradeRowTpl:
    return GradeRowTpl(code=code, stage_name=stage, typical_roles=roles, level_up_criteria=crit)


