def format_grade_plan_rows(track: str, pack: ProfessionPack) -> list[tuple[str, str, str, str]]:
    t = _track(track)
    if len(t) > 72:
        t = t[:69] + "…"
    out: list[tuple[str, str, str, str]] = []
    for row in pack.grade_rows:
        out.append(
            (
                row.code,
                row.stage_name,
                row.typical_roles.format(track=t),
                row.level_up_criteria,
            )
        )
    return out
