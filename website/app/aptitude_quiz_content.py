# Тег профессии из матрицы МТС → набор вопросов (если выбрана целевая роль).
TAG_TO_QUIZ_KEY: dict[str, str] = {
    "IT": "IT",
    "маркетинг": "маркетинг",
    "инженерия": "инженерия",
    "бизнес": "бизнес",
}


def resolve_quiz_key(form_interest: str, target_mts_role_id: str | None) -> str:
    """Приоритет: целевая роль из матрицы → тег роли; иначе интерес из профиля."""
    try:
        from wibe_work.questionnaire_fields import SPHERE_TO_WEB_INTEREST, sphere_to_web_interest

        if form_interest in SPHERE_TO_WEB_INTEREST:
            form_interest = sphere_to_web_interest(form_interest)
        else:
            _legacy = {"поддержка_и_сервис": "medicine", "IT": "it_dev"}
            if form_interest in _legacy:
                form_interest = sphere_to_web_interest(_legacy[form_interest])
    except ImportError:
        pass
    if target_mts_role_id:
        for tr in load_mts_tracks():
            if tr.id == target_mts_role_id:
                mapped = TAG_TO_QUIZ_KEY.get(tr.profession_tag)
                if mapped:
                    return mapped
                break
    if form_interest in INTEREST_QUIZZES:
        return form_interest
    return "общий"


def pick_quiz_questions(form_interest: str, target_mts_role_id: str | None) -> tuple[list[dict], str]:
    key = resolve_quiz_key(form_interest, target_mts_role_id)
    themed = INTEREST_QUIZZES.get(key) or GENERIC_QUIZ
    first = themed[:5] if len(themed) >= 5 else GENERIC_QUIZ[:5]
    bank = (list(first) + GENERIC_QUIZ_PART2)[:QUIZ_TOTAL]
    return bank, key


